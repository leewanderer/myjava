package net.hb.day23;

public class Test04for {
	public static void main(String[] args) {
		System.out.println("for반복문");
		int su=0;
		for(int a=0; a<10; a=a+1) {
			su=su+1;
			System.out.println(su + " ");
		}		
		//for 반복문으로 1~10까지 출력
		System.out.println("\n for 반복문 끝끝");
	}//end	
}//class End
